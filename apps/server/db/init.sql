-- PostgreSQL initialization script for CareBook server
-- Tool/codegen module has been removed from both backend and admin.

DROP TABLE IF EXISTS sys_user_role;
DROP TABLE IF EXISTS sys_user_post;
DROP TABLE IF EXISTS sys_role_menu;
DROP TABLE IF EXISTS sys_role_dept;
DROP TABLE IF EXISTS sys_upload;
DROP TABLE IF EXISTS sys_job_log;
DROP TABLE IF EXISTS sys_job;
DROP TABLE IF EXISTS sys_oper_log;
DROP TABLE IF EXISTS sys_logininfor;
DROP TABLE IF EXISTS sys_user;
DROP TABLE IF EXISTS sys_post;
DROP TABLE IF EXISTS sys_menu;
DROP TABLE IF EXISTS sys_dict_data;
DROP TABLE IF EXISTS sys_dict_type;
DROP TABLE IF EXISTS sys_role;
DROP TABLE IF EXISTS sys_dept;
DROP TABLE IF EXISTS sys_config;

CREATE TABLE sys_config (
  config_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  config_name varchar(100) NOT NULL DEFAULT '',
  config_key varchar(100) NOT NULL DEFAULT '',
  config_value varchar(500) NOT NULL DEFAULT '',
  config_type char(1) NOT NULL DEFAULT 'N',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500),
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0'
);

CREATE TABLE sys_dept (
  dept_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_id integer NOT NULL DEFAULT 0,
  ancestors varchar(50) NOT NULL DEFAULT '0',
  dept_name varchar(30) NOT NULL,
  order_num integer NOT NULL DEFAULT 0,
  leader varchar(20) NOT NULL,
  phone varchar(11) NOT NULL DEFAULT '',
  email varchar(50) NOT NULL DEFAULT '',
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500)
);

CREATE TABLE sys_role (
  role_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role_name varchar(30) NOT NULL,
  role_key varchar(100) NOT NULL,
  role_sort integer NOT NULL DEFAULT 0,
  data_scope char(1) NOT NULL DEFAULT '1',
  menu_check_strictly boolean NOT NULL DEFAULT false,
  dept_check_strictly boolean NOT NULL DEFAULT false,
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500)
);

CREATE TABLE sys_dict_type (
  dict_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  dict_name varchar(100) NOT NULL,
  dict_type varchar(100) NOT NULL UNIQUE,
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500)
);

CREATE TABLE sys_dict_data (
  dict_code integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  dict_sort integer NOT NULL DEFAULT 0,
  dict_label varchar(100) NOT NULL,
  dict_value varchar(100) NOT NULL,
  dict_type varchar(100) NOT NULL,
  css_class varchar(100) NOT NULL DEFAULT '',
  list_class varchar(100) NOT NULL DEFAULT '',
  is_default char(1) NOT NULL DEFAULT 'N',
  status char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500),
  del_flag char(1) NOT NULL DEFAULT '0'
);

CREATE TABLE sys_menu (
  menu_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  menu_name varchar(50) NOT NULL,
  parent_id integer NOT NULL,
  order_num integer NOT NULL DEFAULT 0,
  path varchar(200) NOT NULL DEFAULT '',
  component varchar(255),
  query varchar(255) NOT NULL DEFAULT '',
  is_frame char(1) NOT NULL DEFAULT '1',
  is_cache char(1) NOT NULL DEFAULT '0',
  visible char(1) NOT NULL DEFAULT '0',
  menu_type char(1) NOT NULL DEFAULT 'M',
  perms varchar(100) NOT NULL DEFAULT '',
  icon varchar(100) NOT NULL DEFAULT '',
  status char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500)
);

CREATE TABLE sys_post (
  post_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  post_code varchar(64) NOT NULL,
  post_name varchar(50) NOT NULL,
  post_sort integer NOT NULL DEFAULT 0,
  status char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500),
  del_flag char(1) NOT NULL DEFAULT '0'
);


CREATE TABLE sys_user (
  user_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  dept_id integer,
  user_name varchar(30) NOT NULL,
  nick_name varchar(30) NOT NULL,
  user_type varchar(2) NOT NULL DEFAULT '00',
  email varchar(50) NOT NULL DEFAULT '',
  phonenumber varchar(11) NOT NULL DEFAULT '',
  sex char(1) NOT NULL DEFAULT '0',
  password varchar(200) NOT NULL DEFAULT '',
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0',
  login_ip varchar(128) NOT NULL DEFAULT '',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500),
  avatar varchar(255) NOT NULL DEFAULT '',
  login_date timestamp,
  openid varchar(64) NOT NULL DEFAULT '',
  unionid varchar(64) NOT NULL DEFAULT ''
);

CREATE TABLE sys_logininfor (
  info_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_name varchar(50) NOT NULL DEFAULT '',
  ipaddr varchar(128) NOT NULL DEFAULT '',
  login_location varchar(255) NOT NULL DEFAULT '',
  browser varchar(50) NOT NULL DEFAULT '',
  os varchar(50) NOT NULL DEFAULT '',
  login_time timestamp DEFAULT CURRENT_TIMESTAMP,
  msg varchar(255) NOT NULL DEFAULT '',
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0'
);

CREATE TABLE sys_oper_log (
  oper_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title varchar(50) NOT NULL DEFAULT '',
  business_type integer NOT NULL DEFAULT 0,
  method varchar(100) NOT NULL DEFAULT '',
  request_method varchar(10) NOT NULL DEFAULT '',
  operator_type varchar(10) NOT NULL DEFAULT '0',
  oper_name varchar(50) NOT NULL DEFAULT '',
  dept_name varchar(50) NOT NULL DEFAULT '',
  oper_url varchar(255) NOT NULL DEFAULT '',
  oper_ip varchar(255) NOT NULL DEFAULT '',
  oper_location varchar(255) NOT NULL DEFAULT '',
  oper_param varchar(2000) NOT NULL DEFAULT '',
  json_result varchar(2000) NOT NULL DEFAULT '',
  oper_time timestamp DEFAULT CURRENT_TIMESTAMP,
  status char(1) NOT NULL DEFAULT '0',
  error_msg varchar(2000) NOT NULL DEFAULT '',
  cost_time integer NOT NULL DEFAULT 0
);

CREATE TABLE sys_job (
  job_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  job_name varchar(64) NOT NULL,
  job_group varchar(64) NOT NULL DEFAULT 'DEFAULT',
  invoke_target varchar(500) NOT NULL,
  cron_expression varchar(255),
  misfire_policy varchar(20) DEFAULT '3',
  concurrent char(1) DEFAULT '1',
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500)
);

CREATE TABLE sys_job_log (
  job_log_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  job_name varchar(64) NOT NULL,
  job_group varchar(64) NOT NULL,
  invoke_target varchar(500) NOT NULL,
  job_message varchar(500),
  status char(1) NOT NULL DEFAULT '0',
  exception_info varchar(2000),
  create_time timestamp DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE sys_upload (
  upload_id varchar(255) PRIMARY KEY,
  size integer NOT NULL,
  file_name varchar(255) NOT NULL,
  new_file_name varchar(255) NOT NULL,
  url varchar(255) NOT NULL,
  ext varchar(255),
  status char(1) NOT NULL DEFAULT '0',
  del_flag char(1) NOT NULL DEFAULT '0',
  create_by varchar(64) NOT NULL DEFAULT '',
  create_time timestamp DEFAULT CURRENT_TIMESTAMP,
  update_by varchar(64) NOT NULL DEFAULT '',
  update_time timestamp DEFAULT CURRENT_TIMESTAMP,
  remark varchar(500)
);

CREATE TABLE sys_role_dept (
  role_id integer NOT NULL,
  dept_id integer NOT NULL,
  PRIMARY KEY (role_id, dept_id)
);

CREATE TABLE sys_role_menu (
  role_id integer NOT NULL,
  menu_id integer NOT NULL,
  PRIMARY KEY (role_id, menu_id)
);

CREATE TABLE sys_user_post (
  user_id integer NOT NULL,
  post_id integer NOT NULL,
  PRIMARY KEY (user_id, post_id)
);

CREATE TABLE sys_user_role (
  user_id integer NOT NULL,
  role_id integer NOT NULL,
  PRIMARY KEY (user_id, role_id)
);

